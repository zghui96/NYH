/*A substitution box hash function*/
uint32_t SBox(uint8_t* buf, uint16_t len)
{
	uint32_t hash = 0;
	uint16_t i = 0;

	for (i = 0; i < len; buf++, i++)
	{
		hash ^= SBOX_TABLE[*buf];
		hash *= 3;
	}
	return hash;
}

/*Fowler/Noll/Vo 1 hash function*/
uint32_t FNV1(uint8_t* buf, uint16_t len)
{
	uint32_t p = 16777619;
	uint32_t hash = 2166136261;
	uint16_t i = 0;

	for (i = 0; i < len; buf++, i++)
	{
		hash ^= (*buf);
		hash *= p;
	}

	hash += hash << 13;
	hash ^= hash >> 7;
	hash += hash << 3;
	hash ^= hash >> 17;
	hash += hash << 5;

	return hash;
}

