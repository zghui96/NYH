/* Cyclic Redundancy Check 32*/
uint32_t CRC32(uint8_t* buf, uint16_t len)
{
	uint32_t crc32 = 0;

	for (uint16_t i = 0; i < len; i++)
	{
		crc32 = (crc32 >> 8) ^ CRC32_TABLE[(crc32 ^ buf[i]) & 0xFF];
	}

	return crc32;
}

